package Lesson2;

public class LoopExample {
    public static void main(String[] args) {
        int a = 5;
        while (a <= 10) {
            System.out.println(a);
            a++;
        }
        int b = 15;
        while (b <= 20) {
            System.out.println(b);
            b++;
        }
    }
}
